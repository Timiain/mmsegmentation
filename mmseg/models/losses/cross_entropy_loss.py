def l2_penalty(w,item_count = 6):
    tmp = torch.norm(w,dim=1)
    return torch.abs(torch.sum(tmp)/item_count)

def norm_balance(w):
    p_norms = torch.norm(w,dim=1).view(6).contiguous()
    p = p_norms.repeat(6).view(6,-1).contiguous()
    p_t = p.t()
    tmp =p_t - p
    final_p = torch.abs(tmp)
    norm_balance= torch.sum(final_p)
    return norm_balance

